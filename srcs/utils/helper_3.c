#include "../../inc/minishell.h"

int	only_spaces(char *str)
{
	int	i;

	if (!str)
		return (-1);
	i = -1;
	while (str[++i])
	{
		if (!is_space(str[i]))
			return (0);
	}
	return (1);
}
