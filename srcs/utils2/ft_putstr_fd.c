#include "minishell.h"

void	ft_putstr_fd(char *s, int fd)
{
	char	*p;

	if (!s || fd < 0)
		return ;
	p = s;
	while (*p)
	{
		ft_putchar_fd(*p, fd);
		p++;
	}
}
